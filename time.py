time=int(input("Enter the current time: "))
if time>=5 and time<=11:
    print("The time is Morning now")
elif time>=12 and time<=16:
    print("The time is Afternoon now")
elif time>=17 and time<=20:
    print("The time is Evening now")
elif time>= 21 and time<=24:
    print("The time is Night now")
else:
    print("Time couldn't be identified!")


                                                                                                 